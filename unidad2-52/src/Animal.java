public class Animal {
    
    private String formaReproduccion;
    private String temperaturaSangre;

    public String getFormaReproduccion() {
        return formaReproduccion;
    }
    public void setFormaReproduccion(String formaReproduccion) {
        this.formaReproduccion = formaReproduccion;
    }
    public String getTemperaturaSangre() {
        return temperaturaSangre;
    }
    public void setTemperaturaSangre(String temperaturaSangre) {
        this.temperaturaSangre = temperaturaSangre;
    }

}
