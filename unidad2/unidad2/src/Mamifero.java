public class Mamifero {
    
    private boolean mamanLeche = true;
    private String temperaturaSangre = "Caliente";

    public boolean isMamanLeche() {
        return mamanLeche;
    }

    public void setMamanLeche(boolean mamanLeche) {
        this.mamanLeche = mamanLeche;
    }

    public String getTemperaturaSangre() {
        return temperaturaSangre;
    }

    public void setTemperaturaSangre(String temperaturaSangre) {
        this.temperaturaSangre = temperaturaSangre;
    }
}
